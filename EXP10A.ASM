PRINT MACRO MES
MOV AH,09H
LEA DX,MES
INT 21H
ENDM
.model small
.DATA
MSG1 DB 10,13,"ENTER FIRST STR:$"
MSG2 DB 10,13,"ENTER SECOND STR:$"
MSG3 DB 10,13,"CONCAT STR:$"
STR1 DB 25 DUP('$')
STR2 DB 25 DUP('$')
.CODE
START: MOV AX,@DATA
MOV DS,AX
MOV ES,AX
PRINT MSG1
MOV AH,0AH ;GET FIRST STRING
LEA DX,STR1
INT 21H
PRINT MSG2
MOV AH,0AH ;GET SECOND STRING
LEA DX,STR2
INT 21H
MOV CH,00H ;
MOV CL,STR1+1 ;CX = LENGTH OF STR1
LEA SI,STR1+2 ;SI POINT TO STR1
LEA DI,STR1+2 ;DI POINT TO STR1
CLD

REPZ MOVSB
MOV CH,00H ;
MOV CL,STR2+1 ;CX = LENGHT OF STR2
LEA SI,STR2+2 ;SI POINT TO STR2

REPZ MOVSB
PRINT MSG3
PRINT STR1+2 ;PRINT CONCATE STRING
MOV AH,4CH
INT 21H
END START
